#include <stdio.h>
#define BASE_PAY 25000
#define OT_PAY 40000
int main()
{
  double regWages,
    regHours = 40.0,
    otWages,
    workHours,otHours,
    totalWages;
  printf("How many hours did you work last week:");
  scanf("%lf",&workHours);
  otHours = workHours - regHours;
  regWages = BASE_PAY * regHours;
  otWages = OT_PAY * otHours;
  totalWages = regWages + otWages;
  printf("\n Wages for this week are %.3lf$\n",totalWages);
  return 0;
}
    
