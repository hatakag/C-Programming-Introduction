#include <stdio.h>
int main()
{
  int n,i,j,k;
  printf("Input an interger n (n<8): ");
  scanf("%d",&n);
  if (n>=8)
    printf("Error--n must be less than 8\n");
  else 
    {
      for (i=1;i<=n;i++)
	{
	  for (j=n-i;j>=1;j--)
	    {
	      printf(" ");
	    }
	  for (k=1;k<=2*i-1;k++)
	    {
	      printf("*");
	    }
	  printf("\n");
	}
    }
  return 0;
}
